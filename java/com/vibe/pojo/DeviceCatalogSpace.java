package com.vibe.pojo;


public class DeviceCatalogSpace {
	private AssetVo assetVo;
	private Integer[] catalogs;
	private Integer[] spaceIds;
	public AssetVo getAssetVo() {
		return assetVo;
	}
	public void setAssetVo(AssetVo assetVo) {
		this.assetVo = assetVo;
	}
	public Integer[] getCatalogs() {
		return catalogs;
	}
	public void setCatalogs(Integer[] catalogs) {
		this.catalogs = catalogs;
	}
	public Integer[] getSpaceIds() {
		return spaceIds;
	}
	public void setSpaceIds(Integer[] spaceIds) {
		this.spaceIds = spaceIds;
	}
	
	
}
