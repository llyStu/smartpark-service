package com.vibe.pojo;

public class LinkageLogCauseBean {
	private int id;
	private String time;
	private int causeAsset;
	private int value;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public int getCauseAsset() {
		return causeAsset;
	}
	public void setCauseAsset(int causeAsset) {
		this.causeAsset = causeAsset;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
	
}
