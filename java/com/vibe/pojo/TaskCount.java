package com.vibe.pojo;

public class TaskCount {
	private int unfinishTask;
	private int monthTask;
	private int todayTask;
	public int getUnfinishTask() {
		return unfinishTask;
	}
	public void setUnfinishTask(int unfinishTask) {
		this.unfinishTask = unfinishTask;
	}
	public int getMonthTask() {
		return monthTask;
	}
	public void setMonthTask(int monthTask) {
		this.monthTask = monthTask;
	}
	public int getTodayTask() {
		return todayTask;
	}
	public void setTodayTask(int todayTask) {
		this.todayTask = todayTask;
	}
	
	
}
