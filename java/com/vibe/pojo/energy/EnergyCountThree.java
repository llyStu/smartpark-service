package com.vibe.pojo.energy;

public class EnergyCountThree {
	private String unit;
	private String floor;
	private Double avgStandard;
	private Double data;
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getFloor() {
		return floor;
	}
	public void setFloor(String floor) {
		this.floor = floor;
	}
	public Double getAvgStandard() {
		return avgStandard;
	}
	public void setAvgStandard(Double avgStandard) {
		this.avgStandard = avgStandard;
	}
	public Double getData() {
		return data;
	}
	public void setData(Double data) {
		this.data = data;
	}

}
